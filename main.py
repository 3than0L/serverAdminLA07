def penambahan(a,b):
	total = a+b
	return (total)

def perkalian(a,b):
	total = a*b
	return (total)

def pengurangan(a,b):
	totalPengurangan = a-b
	return (totalPengurangan)

def pembagian(a,b):
	totalPembagian = a/b
	return(totalPembagian)	


def main():
	print (penambahan(5,10))
